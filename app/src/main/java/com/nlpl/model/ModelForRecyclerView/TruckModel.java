package com.nlpl.model.ModelForRecyclerView;

public class TruckModel {
    private String user_id;
    private String vehicle_no;
    private String vehicle_body_type;
    private String rc_book;
    private String vehicle_insurance;
    private String truck_id;

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getVehicle_no() {
        return vehicle_no;
    }

    public void setVehicle_no(String vehicle_no) {
        this.vehicle_no = vehicle_no;
    }

    public String getVehicle_body_type() {
        return vehicle_body_type;
    }

    public void setVehicle_body_type(String vehicle_body_type) {
        this.vehicle_body_type = vehicle_body_type;
    }

    public String getRc_book() {
        return rc_book;
    }

    public void setRc_book(String rc_book) {
        this.rc_book = rc_book;
    }

    public String getVehicle_insurance() {
        return vehicle_insurance;
    }

    public void setVehicle_insurance(String vehicle_insurance) {
        this.vehicle_insurance = vehicle_insurance;
    }

    public String getTruck_id() {
        return truck_id;
    }

    public void setTruck_id(String truck_id) {
        this.truck_id = truck_id;
    }
}
